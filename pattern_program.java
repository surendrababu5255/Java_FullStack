package surendrababu;

public class pattern_program {
	public static void main(String[]args) {
	int n=5,s=1;
	for(int i=1;i<=1;i++) {
	System.out.println("1 2 3 4 5");
	for(int k=1;k<=1;k++) {
		System.out.println((s+1)+"       "+(n-1));
		System.out.println((s+2)+"       "+(n-2));
		System.out.println((s+3)+"       "+(n-3));
		for(int p=1;p>0;p--) {
			System.out.println("5 4 3 2 1");
		}
	}
	}
	}
	}
	

